class Q{    // Logical Operator &&,||
	 public static void main(String[]args){
	 int a=10;
	 int b=2;
	 int c=15;      //True or False    //&& both are true o/p true 
	 Boolean d=((a>b)&&(b==c));	       // || one true va irunthalum o/p true
	 Boolean e=((a>b)||(b<c));
	  System.out.println("And "+ d);
	  System.out.println("OR "+ e);
	  }

}