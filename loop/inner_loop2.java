public class Loops         // Inner for loop                   
{    
public static void main	(String[]args){
	
    
	for(int i=1;i<=5;i++){		// outer  for condition
	  
	  for(int j=1;j<=i;j++){       // Inner for loop
		  
		  System.out.print(j+" ");
	  }
	 System.out.println();
	  }

}
}