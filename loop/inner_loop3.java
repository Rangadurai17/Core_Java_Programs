public class Loops        // Inner for loop                  
  {    
  public static void main	(String[]args){
      
  	for(int i=6;i>=1;i--){    	// outer  for condition
  	
  	
  	
  	  for(int j=5;j>=i;j--){		  // Inner for loop
  	
  		  System.out.print(j+" ");

  	 	 }
  		 System.out.println();
  	}

  }
  }